#!/usr/bin/env node
/**
 * Generates src/lib/country-dial-codes.ts from mledoze countries.json
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const outPath = path.join(__dirname, '../src/lib/country-dial-codes.ts');
const MLEDOZE_URL = 'https://raw.githubusercontent.com/mledoze/countries/master/countries.json';

function extractDialCode(country) {
  const root = country.idd?.root;
  if (!root) return null;
  const suffixes = country.idd.suffixes ?? [];
  if (suffixes.length === 0) return root;
  if (root === '+1' || root.length > 2) return root;
  return `${root}${suffixes[0]}`;
}

const res = await fetch(MLEDOZE_URL);
if (!res.ok) throw new Error(`Failed to fetch: ${res.status}`);
const countries = await res.json();

const byIso = {};
const byDial = new Map();

for (const country of countries) {
  if (!country.cca2 || country.cca2.length !== 2 || country.status === 'historical') continue;
  const dial = extractDialCode(country);
  if (!dial) continue;
  byIso[country.cca2] = dial;
  if (!byDial.has(dial)) byDial.set(dial, []);
  byDial.get(dial).push(country.cca2);
}

const sortedIsos = Object.keys(byIso).sort();
const lines = sortedIsos.map((iso) => `  ${iso}: '${byIso[iso]}',`);

const content = `// Generated by scripts/generate-dial-codes.mjs — do not edit by hand

export const COUNTRY_DIAL_CODES: Record<string, string> = {
${lines.join('\n')}
};

/** ISO codes sharing the same dial code (e.g. +1 for US/CA). */
export const DIAL_CODE_TO_ISOS: Record<string, string[]> = {
${[...byDial.entries()]
  .sort(([a], [b]) => a.localeCompare(b))
  .map(
    ([dial, isos]) =>
      `  '${dial}': [${isos
        .sort()
        .map((i) => `'${i}'`)
        .join(', ')}],`
  )
  .join('\n')}
};

export function getCountryDialCode(isoCode: string): string {
  return COUNTRY_DIAL_CODES[isoCode.toUpperCase()] ?? '';
}

export function resolveIsoForDialCode(dialCode: string, preferredIso?: string): string {
  const normalized = normalizeDialCode(dialCode);
  if (!normalized) return '';
  const isos = DIAL_CODE_TO_ISOS[normalized] ?? [];
  if (isos.length === 0) return '';
  if (preferredIso && isos.includes(preferredIso.toUpperCase())) return preferredIso.toUpperCase();
  return isos[0] ?? '';
}

export function normalizeDialCode(value: string): string {
  const trimmed = value.trim();
  if (!trimmed) return '';
  const digits = trimmed.replace(/[^\d+]/g, '');
  if (!digits) return '';
  const withPlus = digits.startsWith('+') ? digits : \`+\${digits}\`;
  return withPlus.replace(/(?!^)\\+/g, '');
}

export function getAllDialCodeEntries(): { iso: string; dial: string; name?: string }[] {
  return Object.entries(COUNTRY_DIAL_CODES).map(([iso, dial]) => ({ iso, dial }));
}
`;

fs.writeFileSync(outPath, content);
console.log(`Wrote ${outPath} (${sortedIsos.length} countries)`);
